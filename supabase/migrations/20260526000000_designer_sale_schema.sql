-- 8. Create automation_flags table
CREATE TABLE IF NOT EXISTS public.automation_flags (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  entity_type text CHECK (entity_type IN ('sale', 'product')) NOT NULL,
  entity_id bigint NOT NULL,
  flag_type text CHECK (flag_type IN ('expired_date', 'suspected_stale', 'out_of_stock')) NOT NULL,
  raw_signal text,
  flagged_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  reviewed_by uuid REFERENCES auth.users ON DELETE SET NULL,
  reviewed_at timestamp with time zone,
  action_taken text CHECK (action_taken IN ('unpublished', 'marked_ok', 'dismissed'))
);

-- Enable RLS for automation_flags
ALTER TABLE public.automation_flags ENABLE ROW LEVEL SECURITY;

-- Policies for automation_flags
CREATE POLICY "Automation flags are manageable by admins" ON public.automation_flags 
  FOR ALL USING (public.is_admin());

