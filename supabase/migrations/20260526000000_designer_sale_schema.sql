  product_id bigint REFERENCES public.products_secure(id) ON DELETE SET NULL,
  merchant_id bigint REFERENCES public.merchants(id) ON DELETE CASCADE NOT NULL,
  clicked_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  referrer_url text,
  session_id text
);

