  FOR ALL USING (public.is_admin());

-- 6. Create Gated Views with Column Masking
-- For Sales
CREATE OR REPLACE VIEW public.sales AS
SELECT 
  id, merchant_id, title, slug, teaser_text, discount_min_pct, discount_max_pct, sale_start_date, sale_end_date, category_ids, image_urls, is_featured, status, last_verified_at, created_at, updated_at, created_by,
  CASE WHEN auth.role() = 'authenticated' THEN description ELSE NULL END AS description,
  CASE WHEN auth.role() = 'authenticated' THEN sale_url ELSE NULL END AS sale_url,
  CASE WHEN auth.role() = 'authenticated' THEN price_range_min ELSE NULL END AS price_range_min,
  CASE WHEN auth.role() = 'authenticated' THEN price_range_max ELSE NULL END AS price_range_max
FROM public.sales_secure;

-- For Products
CREATE OR REPLACE VIEW public.products AS
SELECT
  id, name, category, image_url, rating, created_at, merchant_id, sale_id, slug, teaser_text, compare_at_price, sku, is_external, updated_at, created_by, status,
  CASE WHEN auth.role() = 'authenticated' THEN price ELSE NULL END AS price,
  CASE WHEN auth.role() = 'authenticated' THEN description ELSE NULL END AS description,
  CASE WHEN auth.role() = 'authenticated' THEN stock_qty ELSE NULL END AS stock_qty,
  CASE WHEN auth.role() = 'authenticated' THEN stripe_price_id ELSE NULL END AS stripe_price_id,
  CASE WHEN auth.role() = 'authenticated' THEN external_url ELSE NULL END AS external_url
FROM public.products_secure;

-- 7. Create affiliate_clicks table
