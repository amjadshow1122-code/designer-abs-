-- DesignerSale.com.au Database Schema Migration
-- Upgrades prototype tables and creates the designer boutique aggregator schema

-- 1. Ensure profiles has is_admin column
ALTER TABLE public.profiles ADD COLUMN IF NOT EXISTS is_admin boolean DEFAULT false;

-- 2. Create is_admin() helper function
CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS boolean AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.profiles 
    WHERE id = auth.uid() AND is_admin = true
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 3. Create merchants table
CREATE TABLE IF NOT EXISTS public.merchants (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  slug text NOT NULL UNIQUE,
  logo_url text,
  website_url text NOT NULL,
  description text,
  category_ids integer[],
  location_city text,
  location_state text,
  social_instagram text,
  status text DEFAULT 'active' CHECK (status IN ('active', 'draft', 'archived')),
  affiliate_url_template text,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  created_by uuid REFERENCES auth.users ON DELETE SET NULL
);

-- Enable RLS for merchants
ALTER TABLE public.merchants ENABLE ROW LEVEL SECURITY;

-- Policies for merchants
CREATE POLICY "Merchants are viewable by everyone" ON public.merchants 
  FOR SELECT USING (true);

CREATE POLICY "Merchants are manageable by admins" ON public.merchants 
  FOR ALL USING (public.is_admin());

-- 4. Create sales_secure table
CREATE TABLE IF NOT EXISTS public.sales_secure (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  merchant_id bigint REFERENCES public.merchants(id) ON DELETE CASCADE NOT NULL,
  title text NOT NULL,
  slug text NOT NULL UNIQUE,
  description text,
  teaser_text text,
  sale_url text NOT NULL,
  discount_min_pct integer,
  discount_max_pct integer,
  price_range_min numeric,
  price_range_max numeric,
  sale_start_date date NOT NULL,
  sale_end_date date,
  category_ids integer[],
  image_urls text[],
  is_featured boolean DEFAULT false NOT NULL,
  status text DEFAULT 'active' CHECK (status IN ('active', 'draft', 'archived', 'expired')),
  last_verified_at timestamp with time zone,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  updated_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  created_by uuid REFERENCES auth.users ON DELETE SET NULL
);

-- Enable RLS for sales_secure
ALTER TABLE public.sales_secure ENABLE ROW LEVEL SECURITY;

-- Policies for sales_secure
CREATE POLICY "Active sales are viewable by everyone" ON public.sales_secure 
  FOR SELECT USING (status = 'active' OR public.is_admin());

CREATE POLICY "Sales are manageable by admins" ON public.sales_secure 
  FOR ALL USING (public.is_admin());

-- 5. Prepare and transition products table
-- Check if products already exists and rename to products_secure
DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM pg_tables WHERE schemaname = 'public' AND tablename = 'products') AND 
     NOT EXISTS (SELECT 1 FROM pg_tables WHERE schemaname = 'public' AND tablename = 'products_secure') THEN
    ALTER TABLE public.products RENAME TO products_secure;
  END IF;
END $$;

