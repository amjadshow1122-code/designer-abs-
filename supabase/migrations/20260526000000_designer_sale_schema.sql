ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS sale_id bigint REFERENCES public.sales_secure(id) ON DELETE SET NULL;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS slug text;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS teaser_text text;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS compare_at_price numeric;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS sku text;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS stock_qty integer DEFAULT 0;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS stripe_price_id text;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS category_ids integer[];
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS image_urls text[];
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS is_external boolean DEFAULT false;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS external_url text;
ALTER TABLE public.products_secure ADD COLUMN IF NOT EXISTS last_verified_at timestamp with time zone;
